/** 根据版本号（如 "0.7.0" 或 "v0.7.0"）取第一条匹配的更新记录，未匹配则返回第一条 */
export function getLatestChangelogEntry(version: string): ChangelogEntry {
  const normalized = version.replace(/^v/i, '')
  const found = changelogEntries.find(
    (e) => e.version.replace(/^v/i, '') === normalized
  )
  return found || changelogEntries[0]
}
